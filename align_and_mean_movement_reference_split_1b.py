def aggregate_reference_sequences(
    stack: np.ndarray,
    method: str = "mean",
    trim_frac: float = 0.10,
) -> np.ndarray:
    """
    Aggregate aligned healthy reference sequences across samples.

    stack shape: (N, T, 108, 3)
    method:
      - mean: current behavior, NaN-aware arithmetic mean
      - median: robust per-coordinate median
      - trimmed_mean: drops low/high sample quantiles per coordinate, then averages
    """
    method = method.strip().lower()
    with np.errstate(all="ignore"):
        if method == "mean":
            return np.nanmean(stack, axis=0)
        if method == "median":
            return np.nanmedian(stack, axis=0)
        if method == "trimmed_mean":
            if not 0.0 <= trim_frac < 0.5:
                raise ValueError("--aggregation_trim_frac must be >= 0 and < 0.5")
            if trim_frac == 0.0 or stack.shape[0] < 3:
                return np.nanmean(stack, axis=0)
            lo = np.nanquantile(stack, trim_frac, axis=0, keepdims=True)
            hi = np.nanquantile(stack, 1.0 - trim_frac, axis=0, keepdims=True)
            trimmed = np.where((stack >= lo) & (stack <= hi), stack, np.nan)
            return np.nanmean(trimmed, axis=0)
    raise ValueError(f"Unknown aggregation method: {method}")

def robust_scale(x: np.ndarray, axis=0) -> np.ndarray:
    med = np.nanmedian(x, axis=axis)
    mad = np.nanmedian(np.abs(x - np.expand_dims(med, axis=axis)), axis=axis)
    scale = 1.4826 * mad
    q25 = np.nanquantile(x, 0.25, axis=axis)
    q75 = np.nanquantile(x, 0.75, axis=axis)
    iqr_scale = (q75 - q25) / 1.349
    scale = np.where(np.isfinite(scale) & (scale > 1e-9), scale, iqr_scale)
    return np.where(np.isfinite(scale) & (scale > 1e-9), scale, 1e-9)

def compute_reference_envelope(
    stack: np.ndarray,
    center_seq: np.ndarray,
    dental_n: int,
    low_q: float,
    high_q: float,
    opp_tau: float = 0.2,
) -> pd.DataFrame:
    face = slice(dental_n, stack.shape[2])
    X = stack[:, :, face, :]
    C = center_seq[:, face, :]

    D = X - X[:, 0:1, :, :]
    Dc = C - C[0:1, :, :]

    mag = np.linalg.norm(D, axis=-1)              # N,T,M
    mag_c = np.linalg.norm(Dc, axis=-1)           # T,M
    amp = np.nanmax(mag, axis=1)                  # N,M

    diff = D - Dc[None, :, :, :]
    rmse = np.sqrt(np.nanmean(np.linalg.norm(diff, axis=-1) ** 2, axis=1))  # N,M

    dot = np.nansum(D * Dc[None, :, :, :], axis=-1)
    denom = (mag * mag_c[None, :, :]) + 1e-9
    cos_sim = dot / denom
    amp_gate = np.nanquantile(np.nanmax(mag_c, axis=0), 0.20)
    amp_gate = max(float(amp_gate), 1e-9)
    dir_ok = np.isfinite(cos_sim) & (mag >= amp_gate) & (mag_c[None, :, :] >= amp_gate)
    neg_part = np.where(dir_ok, np.maximum(0.0, -cos_sim), np.nan)
    w = np.where(dir_ok, np.minimum(mag, mag_c[None, :, :]), np.nan)
    opp_score = np.nansum(w * neg_part, axis=1) / (np.nansum(w, axis=1) + 1e-9)  # N,M
    opp_fraction = np.nanmean(dir_ok & (cos_sim < -opp_tau), axis=1)

    rows = []
    n_markers = X.shape[2]
    for j in range(n_markers):
        rows.append({
            "marker_id": int(j + dental_n),
            "facial_marker_id": int(j),
            "n_reference_samples": int(stack.shape[0]),
            "amp_median": float(np.nanmedian(amp[:, j])),
            "amp_low": float(np.nanquantile(amp[:, j], low_q)),
            "amp_high": float(np.nanquantile(amp[:, j], high_q)),
            "amp_scale": float(robust_scale(amp[:, j], axis=0)),
            "rmse_median": float(np.nanmedian(rmse[:, j])),
            "rmse_high": float(np.nanquantile(rmse[:, j], high_q)),
            "rmse_scale": float(robust_scale(rmse[:, j], axis=0)),
            "opp_score_median": float(np.nanmedian(opp_score[:, j])),
            "opp_score_high": float(np.nanquantile(opp_score[:, j], high_q)),
            "opp_score_scale": float(robust_scale(opp_score[:, j], axis=0)),
            "opp_fraction_median": float(np.nanmedian(opp_fraction[:, j])),
            "opp_fraction_high": float(np.nanquantile(opp_fraction[:, j], high_q)),
        })
    return pd.DataFrame(rows)

def trimmed_indices(residuals: np.ndarray, trim_frac: float) -> np.ndarray:
    """Return boolean mask of inliers as best (1-trim_frac) residuals. residuals shape (N,)."""
    n = residuals.shape[0]
    if n == 0:
        return np.zeros((0,), dtype=bool)
    k = max(1, int(round((1.0 - trim_frac) * n)))
    order = np.argsort(residuals)
    inl = np.zeros((n,), dtype=bool)
    inl[order[:k]] = True
    return inl

def rotation_matrix_from_axis_angle(axis: np.ndarray, angle_rad: float) -> np.ndarray:
    axis = np.asarray(axis, dtype=float)
    norm = np.linalg.norm(axis)
    if norm < 1e-12:
        return np.eye(3)
    axis = axis / norm
    x, y, z = axis
    c = math.cos(angle_rad)
    s = math.sin(angle_rad)
    C = 1 - c
    return np.array([
        [c + x*x*C, x*y*C - z*s, x*z*C + y*s],
        [y*x*C + z*s, c + y*y*C, y*z*C - x*s],
        [z*x*C - y*s, z*y*C + x*s, c + z*z*C],
    ], dtype=float)

def fixed_rot_xyz(deg_xyz: Tuple[float, float, float]) -> np.ndarray:
    """Rotation matrix for successive rotations around X then Y then Z (degrees)."""
    rx, ry, rz = [math.radians(d) for d in deg_xyz]
    Rx = rotation_matrix_from_axis_angle(np.array([1,0,0]), rx)
    Ry = rotation_matrix_from_axis_angle(np.array([0,1,0]), ry)
    Rz = rotation_matrix_from_axis_angle(np.array([0,0,1]), rz)
    return Rz @ Ry @ Rx  # common convention: apply X then Y then Z to column vectors


